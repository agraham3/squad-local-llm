def print_system_info():
    """Print system configuration with Squad-like formatting."""
    _format_header("MULTI-AGENT LOCAL SYSTEM")
    
    _format_section("AVAILABLE MODELS", "MODELS")
    for model_name, model_config in MODELS.items():
        print(f"\n  {model_name}")
        print(f"    - Role: {model_config.role.value}")
        print(f"    - Description: {model_config.description}")
        print(f"    - Settings: temp={model_config.temperature}, max_tokens={model_config.max_tokens}")
    
    _format_section("AVAILABLE AGENTS", "AGENTS")
    for agent_name, agent_config in AGENTS.items():
        print(f"\n  {agent_name}")
        print(f"    - Model: {agent_config.model.name}")
        print(f"    - Role: {agent_config.role}")
        print(f"    - {agent_config.description}")
    
    _format_section("ROUTING RULES", "ROUTING")
    print("\n  - PLANNER: Keywords 'plan', 'design', 'architecture', 'strategy'")
    print("    -> Complex tasks requiring decomposition")
    print("\n  - CODER: Keywords 'implement', 'refactor', 'debug', 'code', 'fix'")
    print("    -> Code generation and complex implementations")
    print("\n  - FAST_UTILITY: Keywords 'quick', 'simple', 'template', 'boilerplate'")
