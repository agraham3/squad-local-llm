def main():
    """Main entry point."""
    parser = argparse.ArgumentParser(
        description="Local multi-agent system with Ollama - Squad Edition"
    )
    parser.add_argument(
        "mode",
        nargs="?",
        choices=["init", "interactive", "example1", "example2", "example3", "info"],
        default="info",
        help="Execution mode"
    )
    parser.add_argument(
        "--task",
        type=str,
        help="Custom task to execute"
    )
    parser.add_argument(
        "--project",
        type=str,
        default=".",
        help="Project root for squad init and file writes"
    )
    
    args = parser.parse_args()
    
    try:
        if args.mode == "init":
            _format_header("INITIALIZING LOCAL SQUAD")
            created = init_squad(args.project)
            if created:
                _format_status("Created runtime files:", "success")
                for path in created:
                    print(f"  {path}")
            else:
                _format_status("Local squad runtime already initialized", "success")
            print("\nRun tasks with: python -m src.main --task \"your task\"\n")

        elif args.task:
            run_custom_task(args.task)

        elif args.mode == "info":
            print_system_info()
            _format_section("USAGE EXAMPLES")
            print("\n  python -m src.main init                # Initialize .squad runtime")
            print("  python -m src.main interactive         # Interactive mode")
            print("  python -m src.main example1            # Planning example")
            print("  python -m src.main example2            # Coding example")
            print("  python -m src.main example3            # Quick boilerplate example")
            print("  python -m src.main --task 'your task'  # Custom task\n")
        
        elif args.mode == "interactive":
            print_system_info()
            interactive_mode()
        
        elif args.mode == "example1":
            example_planning_task()
        
        elif args.mode == "example2":
            example_coding_task()
        
        elif args.mode == "example3":
            example_quick_task()
    
    except KeyboardInterrupt:
        print("\n")
        _format_status("Interrupted by user", "error")
        sys.exit(1)
    except Exception as e:
        print(f"\n")
        _format_status(f"Error: {str(e)}", "error")
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == "__main__":
    main()
