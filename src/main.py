def interactive_mode():
    """Interactive mode with Squad-like interface."""
    _format_section("INTERACTIVE MODE", "INTERACTIVE")
    print("Enter your task (or 'quit' to exit)\n")
    
    execution_count = 0
    
    while True:
        try:
            task = input(">>> ").strip()
            
            if task.lower() in ["quit", "exit", "q"]:
                _format_status("Goodbye!", "success")
                break
            
            if not task:
                print("Please enter a task.\n")
                continue
            
            execution_count += 1
            
            # Show routing info
            assigned_agent = get_routing_agent(task)
            agent_config = AGENTS.get(assigned_agent, AGENTS["planner"])
            model_name = agent_config.model.name
            
            _format_agent_card(assigned_agent, model_name, task)
            
            # Execute
            _format_status("Processing...", "working")
            final_state = invoke_graph(task, verbose=False, save_logs=True)
            
            # Show results
            log_file = _get_latest_log()
            _format_execution_summary(final_state, log_file)
            
            # Show output based on which agent was used
            if final_state.get("plan"):
                _format_box(format_plan_for_display(final_state["plan"])[:500], "PLAN OUTPUT")
            
            if final_state.get("implementation"):
                _format_box(final_state["implementation"][:500], "IMPLEMENTATION OUTPUT")
            
            if final_state.get("utility_output"):
                _format_box(final_state["utility_output"][:500], "UTILITY OUTPUT")

            if final_state.get("files_written"):
                _format_box("\n".join(final_state["files_written"]), "FILES WRITTEN")
            
            if log_file:
                print(f"[LOG] Full execution log: {log_file}\n")
        
        except KeyboardInterrupt:
            print("\n")
            _format_status("Interrupted by user", "error")
            break
        except Exception as e:
            _format_status(f"Error: {str(e)}", "error")
            print()


def run_custom_task(task: str):
    """Execute one user task from the CLI."""
    print_system_info()

    assigned_agent = get_routing_agent(task)
    agent_config = AGENTS.get(assigned_agent, AGENTS["planner"])
    model_name = agent_config.model.name

    _format_agent_card(assigned_agent, model_name, task)

    _format_status("Processing task...", "working")
    final_state = invoke_graph(task, verbose=False, save_logs=True)

    log_file = _get_latest_log()
    _format_execution_summary(final_state, log_file)

    if final_state.get("plan"):
        _format_box(format_plan_for_display(final_state["plan"])[:600], "PLAN OUTPUT")

    if final_state.get("implementation"):
        _format_box(final_state["implementation"][:600], "IMPLEMENTATION OUTPUT")

    if final_state.get("utility_output"):
        _format_box(final_state["utility_output"][:600], "UTILITY OUTPUT")

    if final_state.get("files_written"):
        _format_box("\n".join(final_state["files_written"]), "FILES WRITTEN")

    if log_file:
        _print_log_summary(log_file)


