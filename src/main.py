def _format_header(title: str, width: int = 70):
    """Format and print a main header."""
    print(f"\n{'='*width}")
    print(f"{title.center(width)}")
    print(f"{'='*width}\n")


def _format_section(title: str, symbol: str = ""):
    """Format and print a section divider."""
    if symbol:
        print(f"\n[{symbol}] {title}")
    else:
        print(f"\n[{title}]")
    print(f"{'-'*60}")


def _format_status(text: str, status_type: str = "info"):
    """Print status with symbol."""
    symbols = {
        "info": "[*]",
        "success": "[OK]",
        "error": "[!]",
        "working": "[>]",
        "plan": "[P]",
        "code": "[C]",
        "util": "[U]",
    }
    symbol = symbols.get(status_type, "[*]")
    print(f"{symbol} {text}")


def _format_box(content: str, title: str = "", padding: int = 2):
    """Print content in a formatted box."""
    lines = content.strip().split('\n')
    max_width = max([len(line) for line in lines] + [40])
    
    if title:
        print(f"\n[{title}]")
    
    for line in lines:
        print(f"{'  ' * padding}{line}")
    print()


def _format_agent_card(agent_name: str, model_name: str, task: str):
    """Print a formatted agent assignment card."""
    print(f"\n{'='*60}")
    print(f"  AGENT: {agent_name.upper():<20} MODEL: {model_name}")
    print(f"{'='*60}")
    print(f"  Task: {task[:50]}{'...' if len(task) > 50 else ''}")
    print(f"{'='*60}\n")


def _format_execution_summary(final_state: dict, log_file: str = ""):
    """Print a formatted execution summary."""
    print(f"\n{'='*60}")
    print(f"{'EXECUTION SUMMARY'.center(60)}")
    print(f"{'='*60}\n")
    
    status = final_state.get("status", "unknown")
    task = final_state.get("user_task", "")
    
    _format_status(f"Status: {status}", "success")
    _format_status(f"Task: {task[:60]}{'...' if len(task) > 60 else ''}")
    
    if final_state.get("plan"):
        _format_status("Plan generated by PLANNER", "plan")
    
    if final_state.get("implementation"):
        _format_status("Code generated by CODER", "code")
    
    if final_state.get("utility_output"):
        _format_status("Boilerplate generated by UTILITY", "util")

    files_written = final_state.get("files_written") or []
    if files_written:
        _format_status(f"Files written: {len(files_written)}", "success")

    if final_state.get("file_write_error"):
        _format_status(f"File write error: {final_state['file_write_error']}", "error")
    
    if log_file:
        _format_status(f"Log saved: {log_file}", "success")
    
    print()


def _get_latest_log():
    """Get the most recent log file from .squad/log/"""
    log_dir = Path(".squad/log")
    if not log_dir.exists():
        return None

    markdown_files = [
        path for path in log_dir.glob("*.md")
        if not path.name.startswith("agent_")
    ]
    if markdown_files:
        return str(max(markdown_files, key=lambda path: path.stat().st_mtime))

    json_files = sorted(log_dir.glob("session_*.json"), reverse=True)
    if json_files:
        return str(json_files[0])
    return None


def _print_log_summary(log_file: str):
    """Print a summary of the log file."""
    try:
        log_path = Path(log_file)
        if log_path.suffix.lower() == ".md":
            content = log_path.read_text(encoding="utf-8")
            _format_section("EXECUTION LOG DETAILS")
            preview_lines = []
            for line in content.splitlines():
                if line == "## Result":
                    preview_lines.append(line)
                    break
                if line.startswith("# ") or line.startswith("| **") or line.startswith("## "):
                    preview_lines.append(line)
                if len(preview_lines) >= 12:
                    break

            for line in preview_lines:
                print(line)
            print(f"\nFull log: {log_file}")
            return

        with open(log_file, 'r', encoding="utf-8") as f:
            log_data = json.load(f)
        
        _format_section("EXECUTION LOG DETAILS")
        
        print(f"Timestamp: {log_data.get('timestamp', 'unknown')}")
        print(f"Status: {log_data.get('status', 'unknown')}")
        print(f"Next Agent: {log_data.get('next_agent', 'none')}")
        
        # Show plan if exists
        if log_data.get('plan'):
            print(f"\n[PLAN OUTPUT] (first 300 chars)")
            print(f"{format_plan_for_display(log_data['plan'])[:300]}...")
        
        # Show implementation if exists
        if log_data.get('implementation'):
            print(f"\n[IMPLEMENTATION OUTPUT] (first 300 chars)")
            print(f"{log_data['implementation'][:300]}...")
        
        # Show utility if exists
        if log_data.get('utility_output'):
            print(f"\n[UTILITY OUTPUT] (first 300 chars)")
            print(f"{log_data['utility_output'][:300]}...")
        
        print(f"\nFull log: {log_file}")
    
    except Exception as e:
        print(f"Could not read log: {e}")


